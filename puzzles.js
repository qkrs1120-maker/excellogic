function calcHints(grid) {
  const rows = grid.length;
  const cols = grid[0].length;
  const rowHints = [];
  const colHints = [];
  for (let r = 0; r < rows; r++) {
    const hints = [];
    let count = 0;
    for (let c = 0; c < cols; c++) {
      if (grid[r][c]) { count++; }
      else if (count > 0) { hints.push(count); count = 0; }
    }
    if (count > 0) hints.push(count);
    rowHints.push(hints.length ? hints : [0]);
  }
  for (let c = 0; c < cols; c++) {
    const hints = [];
    let count = 0;
    for (let r = 0; r < rows; r++) {
      if (grid[r][c]) { count++; }
      else if (count > 0) { hints.push(count); count = 0; }
    }
    if (count > 0) hints.push(count);
    colHints.push(hints.length ? hints : [0]);
  }
  return { rowHints, colHints };
}

function makePuzzle(title, grid) {
  const { rowHints, colHints } = calcHints(grid);
  return { title, grid, rowHints, colHints };
}

