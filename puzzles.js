function rand15(title, density) {
  // 시드 기반 결정적 패턴 생성
  const seed = title.split('').reduce((a,c) => a + c.charCodeAt(0), 0);
  const grid = [];
  let s = seed;
  for (let i = 0; i < 15; i++) {
    const row = [];
    for (let j = 0; j < 15; j++) {
      s = (s * 1664525 + 1013904223) & 0xffffffff;
      row.push(((s >>> 0) % 100) < density ? 1 : 0);
    }
    grid.push(row);
  }
  return makePuzzle(title, grid);
}

